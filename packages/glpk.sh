#!/bin/bash
#
# Version and build information for glpk
#
# $Id$
#
######################################################################

######################################################################
#
# Version
#
######################################################################

GLPK_BLDRVERSION=${GLPK_BLDRVERSION:-"4.55"}

######################################################################
#
# Other values
#
######################################################################

GLPK_BUILDS=${GLPK_BUILDS:-"ser"}
GLPK_DEPS=

######################################################################
#
# Add to path
#
######################################################################

addtopathvar PATH $CONTRIB_DIR/glpk/bin

######################################################################
#
# Launch glpk builds.
#
######################################################################

buildGlpk() {
  if bilderUnpack glpk; then
    if bilderConfig glpk ser; then
      bilderBuild glpk ser
    fi
  fi
}

######################################################################
#
# Test glpk
#
######################################################################

testGlpk() {
  techo "Not testing glpk."
}

######################################################################
#
# Install glpk
#
######################################################################

installGlpk() {
  bilderInstall -r glpk ser glpk
}

