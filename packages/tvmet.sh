#!/bin/bash
#
# Version and build information for tvmet
#
# $Id$
#
######################################################################

######################################################################
#
# Version
#
######################################################################
TVMET_BLDRVERSION=${TVMET_BLDRVERSION:-"1.7.2"}


######################################################################
#
# Other values
#
######################################################################

if test -z "$TVMET_BUILDS"; then
  TVMET_BUILDS=ser
fi

TVMET_DEPS=autotools
TVMET_UMASK=002

######################################################################
#
# Add to paths
#
######################################################################

######################################################################
#
# Launch tvmet builds.
#
######################################################################

buildTvmet() {
  if bilderUnpack tvmet; then
    if bilderConfig tvmet ser; then
      bilderBuild tvmet ser
    fi
  fi
}

######################################################################
#
# Test tvmet
#
######################################################################

testTvmet() {
  techo "Not testing tvmet."
}

######################################################################
#
# Install tvmet
#
######################################################################

installTvmet() {
  bilderInstall tvmet ser
}
